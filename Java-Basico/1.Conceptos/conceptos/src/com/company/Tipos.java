package com.company;

public class Tipos {

    public static void main(String[] args) {

        // tipo identificador = 30;
        // tipo identificador;
        // identificador = 30;

        // enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 byte
        int number3 = 3; // 4 byte
        long number4 = 4; // 8 byte

        // punto flotante
        float decimal1 = 4.9f; // si no ponemos la 'f' al final asigna  por defecto tipo double
        double decimal2 = 9.99;

        // caracter
        char caracter1 = 'a';

        // booleanos
        boolean verdadero = true;
        boolean falso = false;

        // cadenas de taxto
        String nombre = "Alan";
        String apellido = "Sastre";

        // tipos envoltorio
        Integer numero1 = null;
        Long numero2 = 2L;
    }
}
